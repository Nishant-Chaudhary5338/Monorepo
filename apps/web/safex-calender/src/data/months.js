export const getMonthData = (id) => {
  const index = months.findIndex((m) => m.id === id);
  if (index === -1) return { current: null, previous: null, next: null };
  return {
    current: months[index],
    previous: months[index - 1] || null,
    next: months[index + 1] || null,
  };
};
